    public String getContentType(String fileName) {
        String end = fileName.contains(".") ? fileName.substring(fileName.lastIndexOf(".")).toLowerCase() : ".*";
        return mTypeMap.containsKey(end) ? mTypeMap.get(end) : mTypeMap.get(".*");
    }


